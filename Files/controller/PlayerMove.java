package controller;

public class PlayerMove {
    private int l = 0;
    private int r = 0;
    private int u = 0;
    private int d = 0;
    private char dir;

    /**
     * getter for l
     * @return int l
     */
    public int getL() {
        return l;
    }

    /**
     * setter for l
     * @param l l
     */
    public void setL(int l) {
        this.l = l;
    }

    /**
     * getter for r
     * @return int r
     */
    public int getR() {
        return r;
    }

    /**
     * setter for r
     * @param r r
     */
    public void setR(int r) {
        this.r = r;
    }

    /**
     * getter for u
     * @return int u
     */
    public int getU() {
        return u;
    }

    /**
     * setter for u
     * @param u u
     */
    public void setU(int u) {
        this.u = u;
    }

    /**
     * getter for d
     * @return int d
     */
    public int getD() {
        return d;
    }

    /**
     * setter for d
     * @param d d
     */
    public void setD(int d) {
        this.d = d;
    }

    /**
     * getter for dir
     * @return int dir
     */
    public char getDir() {
        return dir;
    }

    /**
     * setter for dir
     * @param dir dir
     */
    public void setDir(char dir) {
        this.dir = dir;
    }


}
