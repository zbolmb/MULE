package Controller;

public class PlayerMove {
    private int l = 0;
    private int r = 0;
    private int u = 0;
    private int d = 0;
    private char dir;

    public int getL() {
        return l;
    }
    public void setL(int l) {
        this.l = l;
    }
    public int getR() {
        return r;
    }
    public void setR(int r) {
        this.r = r;
    }
    public int getU() {
        return u;
    }
    public void setU(int u) {
        this.u = u;
    }
    public int getD() {
        return d;
    }
    public void setD(int d) {
        this.d = d;
    }
    public char getDir() {
        return dir;
    }
    public void setDir(char dir) {
        this.dir = dir;
    }


}
